import random
import turtle 
import winsound

tela = turtle.Screen()
tela.title("Pong Weezer por @JoaoAHaupt")
tela.bgcolor("#189bcc") #Blue Album 
tela.setup(width=1000, height=800)
tela.tracer(0)

pontos_taco_1 = 0
pontos_taco_2 =0

# Taco 1
taco_1 = turtle.Turtle()
taco_1.speed(0)
taco_1.shape("square")
taco_1.color("black")
taco_1.shapesize(stretch_wid=5, stretch_len=1)
taco_1.penup() # Tira o marcador do turtle
taco_1.goto(-450, 0)

# Taco 2
taco_2 = turtle.Turtle()
taco_2.speed(0)
taco_2.shape("square")
taco_2.color("black")
taco_2.shapesize(stretch_wid=5, stretch_len=1)
taco_2.penup() # Tira o marcador do turtle
taco_2.goto(450, 0)

# Bola
bola = turtle.Turtle()
bola.speed(0)
bola.shape("circle")
bola.color("white")
bola.penup() # Tira o marcador do turtle
bola.goto(0, 0)
bola.dx = 0.25
bola.dy = -0.25

pen = turtle.Turtle()
pen.speed(0)
pen.color("black")
pen.penup()
pen.hideturtle()
pen.goto(0, 350)
pen.write("Rivers Cuomo: 0  Brian Bell: 0 " , align= "center", font=("Courier", 30))

def taco_1_up():
    y = taco_1.ycor() #Retorna a cordenada de y
    y += 20
    taco_1.sety(y)


def taco_1_down():
    y = taco_1.ycor() #Retorna a cordenada de y
    y -= 20
    taco_1.sety(y)



tela.listen()
tela.onkeypress(taco_1_up, "w")
tela.onkeypress(taco_1_down, "s")




#Loop do jogo
while True:
    tela.update() 
    bola.setx(bola.xcor() + bola.dx)
    bola.sety(bola.ycor() + bola.dy)
    taco_2.sety(bola.ycor() - 40.5)
    if taco_1.ycor() > 350:
        taco_1.sety(350)
    if taco_1.ycor() < -350:
        taco_1.sety(-350)
    

    if taco_2.ycor() > 350:
        taco_2.sety(350)
    if taco_2.ycor() < -350:
        taco_2.sety(-350)

    # bater na borda 
    if bola.ycor() > 400:
        bola.sety(400)
        bola.dy *= -1
    elif bola.ycor() < -400:
        bola.sety(-400)
        bola.dy *= -1
    
    if bola.xcor() > 500:
        bola.goto(0, 0)
        bola.dx *= -1
        pontos_taco_1+=1
        pen.clear()
        pen.write("Rivers Cuomo: {}   Brian Bell: {}".format(pontos_taco_1, pontos_taco_2) , align= "center", font=("Courier", 30))

    if bola.xcor() < -500:
        bola.goto(0, 0)
        bola.dx *= -1
        pontos_taco_2+=1
        pen.clear()
        pen.write("Rivers Cuomo: {}   Brian Bell: {}".format(pontos_taco_1, pontos_taco_2) , align= "center", font=("Courier", 30))



    if bola.xcor() > 450 and bola.xcor() > taco_2.xcor() and(bola.ycor() < taco_2.ycor() + 50 and bola.ycor() > taco_2.ycor() - 50): 
        bola.setx(450)
        bola.dx *= -1

    
    if bola.xcor() < -450 and (bola.ycor() < taco_1.ycor() + 40 and bola.ycor() > taco_1.ycor() - 40): 
        bola.setx(-450)
        bola.dx *= -1
    



    
    




